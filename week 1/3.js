// learn about Operators (Arithmetic, Assignment, Comparison) and simple calculations.
// Javascript operators are used to perform different types of mathematical and logical computations.

// Examples:
// The Assignment Operator = assigns values

// The Addition Operator + adds values

// The Multiplication Operator * multiplies values

// The Comparison Operator > compares values


let num1 = 10;
let num2 = 5;

console.log(num1 + num2); // Addition: 15
console.log(num1 - num2); // Subtraction: 5
console.log(num1 * num2); // Multiplication: 50
console.log(num1 / num2); // Division: 2
console.log(num1 % num2); // Modulus: 0



console.log(2 + 3 * 4); // 14 (Multiplication first)
console.log((2 + 3) * 4); // 20 (Brackets first)
